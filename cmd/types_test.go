func TestTypes_ProjectFallbackFromConfig(t *testing.T) {
	srv := typesFakeServer(t)
	defer srv.Close()

	home := t.TempDir()
	t.Setenv("HOME", home)
	cfgDir := filepath.Join(home, ".flagify")
	if err := os.MkdirAll(cfgDir, 0o700); err != nil {
		t.Fatalf("mkdir config dir: %v", err)
	}
	cfg := map[string]any{
		"accessToken": "test-token",
		"apiUrl":      srv.URL,
		"projectId":   "proj_01",
	}
	data, _ := json.Marshal(cfg)
	if err := os.WriteFile(filepath.Join(cfgDir, "config.json"), data, 0o600); err != nil {
		t.Fatalf("write config: %v", err)
	}
	resetTypesFlags(t)

	outFile := filepath.Join(t.TempDir(), "flags.ts")
	if _, err := runRoot(t, "types", "--typescript", "-o", outFile); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	data, err := os.ReadFile(outFile)
	if err != nil {
		t.Fatalf("read output: %v", err)
	}
	if !strings.Contains(string(data), "new-checkout-flow") {
		t.Errorf("expected flags from proj_01, got:\n%s", data)
	}
}

func TestTypes_OverwriteUnknownFileRequiresConfirm(t *testing.T) {
	srv := typesFakeServer(t)
	defer srv.Close()
	writeTestConfig(t, srv.URL)
	resetTypesFlags(t)

	outFile := filepath.Join(t.TempDir(), "flags.ts")
	if err := os.WriteFile(outFile, []byte("// hand-authored, do not clobber\nexport const foo = 1\n"), 0o600); err != nil {
		t.Fatalf("seed file: %v", err)
	}

	// In a non-TTY test context, ui.Confirm returns true (skip=true path via
	// terminal check), so the write proceeds. This test documents that
	// contract: non-TTY callers get silent overwrites, and the regression we
	// guard against is human users losing hand-authored files at a prompt.
	if _, err := runRoot(t, "types", "--typescript", "-o", outFile, "-p", "proj_01"); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	data, _ := os.ReadFile(outFile)
	if !strings.Contains(string(data), "Auto-generated by Flagify CLI") {
		t.Errorf("expected file to be overwritten with generated content:\n%s", data)
	}
}

