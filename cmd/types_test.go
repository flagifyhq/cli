func TestTypes_DefaultOutputName(t *testing.T) {
	srv := typesFakeServer(t)
	defer srv.Close()
	writeTestConfig(t, srv.URL)
	resetTypesFlags(t)

	// Run in a temp dir so the default output lands there.
	tmp := t.TempDir()
	orig, err := os.Getwd()
	if err != nil {
		t.Fatalf("getwd: %v", err)
	}
	if err := os.Chdir(tmp); err != nil {
		t.Fatalf("chdir: %v", err)
	}
	t.Cleanup(func() { _ = os.Chdir(orig) })

	if _, err := runRoot(t, "types", "--typescript", "-p", "proj_01"); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if _, err := os.Stat(filepath.Join(tmp, "flagify.ts")); err != nil {
		t.Fatalf("default file not created: %v", err)
	}
}
