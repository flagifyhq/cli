func TestTypes_TypeScript_HappyPath(t *testing.T) {
	srv := typesFakeServer(t)
	defer srv.Close()
	writeTestConfig(t, srv.URL)
	resetTypesFlags(t)

	outFile := filepath.Join(t.TempDir(), "flags.ts")
	if _, err := runRoot(t, "types", "--typescript", "-o", outFile, "-p", "proj_01"); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	data, err := os.ReadFile(outFile)
	if err != nil {
		t.Fatalf("read output: %v", err)
	}
	got := string(data)

	for _, want := range []string{
		"// Auto-generated by Flagify CLI",
		"// Project: Web App",
		"export const FLAG_KEYS",
		`"dark-mode": "dark-mode"`,
		`"new-checkout-flow": "new-checkout-flow"`,
		"export type FlagKey = keyof typeof FLAG_KEYS;",
	} {
		if !strings.Contains(got, want) {
			t.Errorf("missing %q in output:\n%s", want, got)
		}
	}
	// Alphabetical: dark-mode before new-checkout-flow.
	if strings.Index(got, "dark-mode") > strings.Index(got, "new-checkout-flow") {
		t.Errorf("keys not alphabetically sorted:\n%s", got)
	}
}

