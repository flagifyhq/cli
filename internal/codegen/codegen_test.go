func flagsFromKeys(keys ...string) []api.Flag {
	out := make([]api.Flag, 0, len(keys))
	for _, k := range keys {
		out = append(out, api.Flag{Key: k})
	}
	return out
}

func TestKebabToPascal(t *testing.T) {
	cases := map[string]string{
		"new-checkout-flow":  "NewCheckoutFlow",
		"dark-mode":          "DarkMode",
		"onboarding-v2":      "OnboardingV2",
		"enable-ai-features": "EnableAiFeatures",
		"x":                  "X",
		"":                   "",
		"a-b":                "AB",
	}
	for in, want := range cases {
		if got := kebabToPascal(in); got != want {
			t.Errorf("kebabToPascal(%q) = %q, want %q", in, got, want)
		}
	}
}

func TestGenerateTypeScript_SortsAlphabetically(t *testing.T) {
	out := GenerateTypeScript(flagsFromKeys("zeta", "alpha", "mike"), "Web App")

	if !strings.Contains(out, "// Project: Web App") {
		t.Fatalf("missing project header:\n%s", out)
	}
	if !strings.Contains(out, "// Generated: 2026-04-19T00:00:00Z") {
		t.Fatalf("missing generated stamp:\n%s", out)
	}
	aIdx := strings.Index(out, `"alpha"`)
	mIdx := strings.Index(out, `"mike"`)
	zIdx := strings.Index(out, `"zeta"`)
	if !(aIdx < mIdx && mIdx < zIdx) {
		t.Errorf("keys not sorted alphabetically: a=%d m=%d z=%d", aIdx, mIdx, zIdx)
	}
	if !strings.Contains(out, "export type FlagKey = keyof typeof FLAG_KEYS;") {
		t.Errorf("missing FlagKey type:\n%s", out)
	}
}

