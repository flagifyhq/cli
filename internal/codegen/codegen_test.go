func TestGenerateGo_BasicShape(t *testing.T) {
	out, err := GenerateGo(flagsFromKeys("new-checkout-flow", "dark-mode"), "Web App", "flagify")
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	for _, want := range []string{
		"// Auto-generated by Flagify CLI — do not edit manually",
		"// Project: Web App",
		"package flagify",
		`FlagDarkMode`,
		`= "dark-mode"`,
		`FlagNewCheckoutFlow = "new-checkout-flow"`,
		"const (",
	} {
		if !strings.Contains(out, want) {
			t.Errorf("missing %q in output:\n%s", want, out)
		}
	}

	// Sorted by key (= by identifier since Flag prefix is constant).
	darkIdx := strings.Index(out, "FlagDarkMode")
	newIdx := strings.Index(out, "FlagNewCheckoutFlow")
	if darkIdx > newIdx {
		t.Errorf("Go consts not alphabetical: dark=%d new=%d", darkIdx, newIdx)
	}
}

